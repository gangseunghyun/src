package DAY03;

public class ForSwitchEX {
    public static void main(String[] args) {
        for (int i = 0; i < 3; i++) {
            int num = i;
            switch (num) {
                case 0:
                    System.out.println("Zero");

                case 1:
                    System.out.println("One");

            }
        }
    }
}
